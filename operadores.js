// mesmos operadores que uma calculadora usa.
// +(adição), -(subtração), *(multiplicação), /(divisão), %(resto da divisão), **(exponenciação).
/*
os operadores mais utilizados serão os de comparação:

< menor que
> maior que
=== igual a (compara tipo e valor)
!== diferente de (compara tipo e valor)
<= menor ou igual
>= maior ou igual
|| ou (verdadeiro ou falso)
&& e (verdadeiro e verdadeiro)

*/
console.log("1 + 1 é:", 1+1)
console.log("2 - 4 é:", 2-4)
console.log("2 * 6 é:", 2*6)
console.log("2 / 4 é:", 2/4)
console.log("2 % 4 é:", 2%4)
console.log("2 ** 4 é:", 2**4)

console.log("\n--- Operardores de comparação ---\n") // \n da um enter na linha
